const withHeaders = str =>
  str.split("[").map((substr, i) =>
    substr.split("]").map((content, j) => {
      if (j === 0) {
        return (
          <p key={"content" + i + "-" + j}>
            <strong>{content}</strong>
          </p>
        );
      } else {
        return <p key={"content" + i + "-" + j}>{content} <br/><br/></p>;
      }
    })
  );

const PrivacyPolicy = () => {
  return (
    <div className={classes.Container}>
      {withHeaders(PrivacyPolicyText)}
    </div>
  );
};
