const withHeaders = str =>
  str.split("[").map((substr, i) =>
    substr.split("]").map((content, j) => {
      if (j === 0) {
        return (
          <p key={"content" + i + "-" + j}>
            <strong>{content}</strong>
          </p>
        );
      } else {
        const formatContent = content
          .split(/[.]\n/)
          .filter(line => line && line !== "\n")
          .map((line, k) => (
            <Aux key={"line" + i + j + k}>
              {line + "."}
              <br />
            </Aux>
          ));
        return (
          <p key={"content" + i + "-" + j}>
            {formatContent}
            <br />
          </p>
        );
      }
    })
  );

